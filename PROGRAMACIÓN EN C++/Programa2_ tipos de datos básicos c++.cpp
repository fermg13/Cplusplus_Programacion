#include <iostream>
using namespace std;

int main(){
    int entero = 15;  //se utiliza la variable numero, una variable es un espacio en memoria donde se almacena un valor determinado.
    float flotante = 10.45; //Es para los numeros decimales.
    double mayor = 16.3456; //es parecido al float, en este se pueden guardar o asignar numeros más extensos.
    char letra = 'a'; //char es una variable tipo caracter de la cual se puede imprimir cualquier caracter que se quiera (numero/letra). Toda variable debe tener nombre (letra).
    cout<<entero<<endl;
    cout<<flotante<<endl;
    cout<<mayor<<endl;
    cout<<letra<<endl;
    return 0;
}
